// –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ placeholder-–æ–≤ —Ü–µ–Ω –¥–ª—è –≤—ã–±—Ä–∞–Ω–Ω–æ–π –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
function updatePricePlaceholders() {
    const categoryProducts = products.filter(product => {
        return currentCategory === '' ||
               product.category.toLowerCase() === currentCategory.toLowerCase();
    }).filter(product => parsePrice(product.price) !== null);

    if (categoryProducts.length > 0) {
        const prices = categoryProducts.map(product => parsePrice(product.price));
        const min = Math.min(...prices);
        const max = Math.max(...prices);
        document.getElementById('minPrice').placeholder = `–û—Ç ${min}`;
        document.getElementById('maxPrice').placeholder = `–î–æ ${max}`;
    } else {
        document.getElementById('minPrice').placeholder = "–û—Ç";
        document.getElementById('maxPrice').placeholder = "–î–æ";
    }
}

// –ü—Ä–∏–º–µ–Ω–µ–Ω–∏–µ —Ñ–∏–ª—å—Ç—Ä–æ–≤ –∏ —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∏
function applyFiltersAndSort() {
    let filtered = products.filter(product => {
        // –§–∏–ª—å—Ç—Ä –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
        if (currentCategory && product.category.toLowerCase() !== currentCategory.toLowerCase()) {
            return false;
        }
        // –§–∏–ª—å—Ç—Ä –ø–æ —Ü–µ–Ω–µ
        const price = parsePrice(product.price);
        if (price === null) return false;
        if (currentMinPrice !== null && price < currentMinPrice) return false;
        if (currentMaxPrice !== null && price > currentMaxPrice) return false;
        // –§–∏–ª—å—Ç—Ä –ø–æ –ø–æ–∏—Å–∫–æ–≤–æ–º—É –∑–∞–ø—Ä–æ—Å—É
        if (currentSearchQuery && !product.name.toLowerCase().includes(currentSearchQuery)) {
            return false;
        }
        return true;
    });

    // –°–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
    if (currentSortOrder === 'asc') {
        filtered.sort((a, b) => parsePrice(a.price) - parsePrice(b.price));
    } else if (currentSortOrder === 'desc') {
        filtered.sort((a, b) => parsePrice(b.price) - parsePrice(a.price));
    }

    renderProducts(filtered);
}

// –ò–∑–º–µ–Ω–µ–Ω–∏–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
function filterCategory(category) {
    currentCategory = category;
    updatePricePlaceholders();
    applyFiltersAndSort();
}

// –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ —Ç–æ–≤–∞—Ä–æ–≤ –Ω–∞ –∫–Ω–æ–ø–∫–∞—Ö –∫–∞—Ç–µ–≥–æ—Ä–∏–π
function getProductCount(category) {
    if (category === '') return products.length;
    return products.filter(product => product.category.toLowerCase() === category.toLowerCase()).length;
}

function updateCategoryButtons() {
    const categories = ['–í—Å–µ', '–î–∏–≤–∞–Ω—ã', '–£–≥–ª—ã', '–ö—Ä–µ—Å–ª–∞', '–ö—É—Ö–æ–Ω–Ω—ã–µ —É–≥–ª—ã', '–ö–æ–º–ø–ª–µ–∫—Ç—ã', '–ö—Ä–æ–≤–∞—Ç–∏'];
    categories.forEach(category => {
        const button = document.getElementById(category);
        const count = getProductCount(category === '–í—Å–µ' ? '' : category);
        button.textContent = `${category} (${count})`;
    });
}

